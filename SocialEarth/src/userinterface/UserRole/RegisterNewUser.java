    private void signupBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupBtnActionPerformed
        String name = nameField.getText();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Name cannot be empty!", "Warning!", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String signupUsername = signupUsernameField.getText();
        if (signupUsername.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Username cannot be empty!", "Warning!", JOptionPane.WARNING_MESSAGE);
            return;
        }
        char[] passwordCharArray = signupPasswordField.getPassword();
        String password = String.valueOf(passwordCharArray);
        if (password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Password cannot be empty!", "Warning!", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Employee employee = system.getEmployeeDirectory().createEmployee(name);
        UserAccount useraccount = system.getUserAccountDirectory().createUserAccount(signupUsername, password, employee, new UserRole());

        JOptionPane.showMessageDialog(null, "Successfully registered!");
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        userProcessContainer.add("registerSuccess", new RegisterSuccess());
        layout.next(userProcessContainer);
    }//GEN-LAST:event_signupBtnActionPerformed


